package DrRAJ.Bean;

public class RelatedProductBean {

	private String reletedProductId;
	private String reletedId;
	private String productId;
	private String reletedProductName;
	private String productName;

	public String getReletedProductId() {
		return reletedProductId;
	}

	public void setReletedProductId(String reletedProductId) {
		this.reletedProductId = reletedProductId;
	}

	public String getReletedId() {
		return reletedId;
	}

	public void setReletedId(String reletedId) {
		this.reletedId = reletedId;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getReletedProductName() {
		return reletedProductName;
	}

	public void setReletedProductName(String reletedProductName) {
		this.reletedProductName = reletedProductName;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

}
