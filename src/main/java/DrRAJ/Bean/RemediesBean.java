package DrRAJ.Bean;

public class RemediesBean {

	private String remediesId;
	private String name;
	private String photolink;
	private String forwardLink;

	public String getForwardLink() {
		return forwardLink;
	}

	public void setForwardLink(String forwardLink) {
		this.forwardLink = forwardLink;
	}

	public String getRemediesId() {
		return remediesId;
	}

	public void setRemediesId(String remediesId) {
		this.remediesId = remediesId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhotolink() {
		return photolink;
	}

	public void setPhotolink(String photolink) {
		this.photolink = photolink;
	}

}
