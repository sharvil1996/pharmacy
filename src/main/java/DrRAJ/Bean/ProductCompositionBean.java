package DrRAJ.Bean;

public class ProductCompositionBean {

	private String productCompositionId;
	private String productId;
	private String productName;
	private String compositionContent;
	private String ingredientsId;
	private String ingredientsName;

	public String getProductCompositionId() {
		return productCompositionId;
	}

	public void setProductCompositionId(String productCompositionId) {
		this.productCompositionId = productCompositionId;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getCompositionContent() {
		return compositionContent;
	}

	public void setCompositionContent(String compositionContent) {
		this.compositionContent = compositionContent;
	}

	public String getIngredientsId() {
		return ingredientsId;
	}

	public void setIngredientsId(String ingredientsId) {
		this.ingredientsId = ingredientsId;
	}

	public String getIngredientsName() {
		return ingredientsName;
	}

	public void setIngredientsName(String ingredientsName) {
		this.ingredientsName = ingredientsName;
	}

}
