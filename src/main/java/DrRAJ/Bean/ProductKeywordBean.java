package DrRAJ.Bean;

public class ProductKeywordBean {

	private String productId;
	private String productName;
	private String keyword;
	private String productKeywordId;

	public String getProductKeywordId() {
		return productKeywordId;
	}

	public void setProductKeywordId(String productKeywordId) {
		this.productKeywordId = productKeywordId;
	}

	public String getKeyword() {
		return keyword;
	}

	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

}
