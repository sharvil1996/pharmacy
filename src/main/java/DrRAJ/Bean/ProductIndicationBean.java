package DrRAJ.Bean;

public class ProductIndicationBean {

	private String productIndicationId;
	private String productId;
	private String productName;
	private String indication;

	public String getProductIndicationId() {
		return productIndicationId;
	}

	public void setProductIndicationId(String productIndicationId) {
		this.productIndicationId = productIndicationId;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(String productId) {
		this.productId = productId;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getIndication() {
		return indication;
	}

	public void setIndication(String indication) {
		this.indication = indication;
	}

}
