package DrRAJ.Bean;

public class CompanyManagementBean {
	private String companyManagementId;
	private String photoLink;
	private String name;
	private String description;
	private String title;

	public String getCompanyManagementId() {
		return companyManagementId;
	}

	public void setCompanyManagementId(String companyManagementId) {
		this.companyManagementId = companyManagementId;
	}

	public String getPhotoLink() {
		return photoLink;
	}

	public void setPhotoLink(String photoLink) {
		this.photoLink = photoLink;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}
}
