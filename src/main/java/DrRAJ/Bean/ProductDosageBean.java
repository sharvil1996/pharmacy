package DrRAJ.Bean;

public class ProductDosageBean {

	private String productDosageId;
	private String productId;
	private String productName;
	private String content;
	public String getProductDosageId() {
		return productDosageId;
	}
	public void setProductDosageId(String productDosageId) {
		this.productDosageId = productDosageId;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}

}
