package DrRAJ.Bean;

public class PressInfoBean {

	private String pressInfoId;
	private String typeId;
	private String title;
	private String link;
	private String pressTypeName;

	public String getPressInfoId() {
		return pressInfoId;
	}

	public void setPressInfoId(String pressInfoId) {
		this.pressInfoId = pressInfoId;
	}

	public String getTypeId() {
		return typeId;
	}

	public void setTypeId(String typeId) {
		this.typeId = typeId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public String getPressTypeName() {
		return pressTypeName;
	}

	public void setPressTypeName(String pressTypeName) {
		this.pressTypeName = pressTypeName;
	}

}
